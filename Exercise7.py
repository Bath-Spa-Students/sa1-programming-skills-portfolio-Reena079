# Counting from 0 to 50 upwards
print("Counting up from 0 to 50 in increments of 1:")
for i in range (0,51,1):
    print(i,end=" ")
print("\n")

#Counting from 0 to 50 downwards
print("Counting down from 50 to 0 in decrements of 1:")
for i in range(50,-1,-1):
   print(i,end=" ")
print("\n")

#Counting up from 30 to 50 upwards
print("Counting up from 30 to 50 in increments of 1:")
for i in range(30,51,1):
    print(i,end=" ")
print("\n")

# counting downwards from 50 to 10 by skipping 2 numbers
print("Counting down from 50 to 10 in decrements of 2:")
